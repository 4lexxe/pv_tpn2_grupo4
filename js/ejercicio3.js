const radio = 7.5;

// Calcular el área usando Math.PI para obtener el valor de π
const area = Math.PI * Math.pow(radio, 2);

// Mostrar el resultado por consola
console.log("Datos del círculo:");
console.log("Radio:", radio);
console.log("Área:", area.toFixed(2)); // toFixed(2) es el metodo para mostrar solo 2 decimales

